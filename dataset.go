package main

import (
	"encoding/json"
	"io/ioutil"
)

// A DataSet maps two-letter codes to possible words.
type DataSet map[string][]string

// ReadDataSet reads a DataSet from a JSON file.
func ReadDataSet(path string) (DataSet, error) {
	var ds DataSet
	data, err := ioutil.ReadFile(path)
	if err != nil {
		return nil, err
	}
	if err := json.Unmarshal(data, &ds); err != nil {
		return nil, err
	}
	return ds, nil
}

// Write stores the data to a JSON file.
func (d DataSet) Write(path string) error {
	encoded, err := json.Marshal(d)
	if err != nil {
		return err
	}
	return ioutil.WriteFile(path, encoded, 0755)
}

